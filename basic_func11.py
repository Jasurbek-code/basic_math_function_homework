# Create a function called main.
# Create function argument a.
# Return the absolute value of a.

# Example:
# Input: a = -8
# Output: 8

def main(a):        # funksiya a argument bilan yaratildi
    return abs(a)   # a argument faqat musbat qiymat qaytarish uchun abs() funksiyani ishlatamiz

print(main(-8))     # funksiyani chiqarish


